        <h3>General</h3>

        <details class="faq-item" open>
            <summary>What is Life Drawing Randburg about?</summary>
            <div class="faq-answer">
                <p>A casual space that is conducive to going deep with your/our art practice, that is safe but also brave, inclusive, diverse and authentically creative. Basically, it is about artistry in as many ways as there are participants.</p>
                <p>I&rsquo;ve been running regular life drawing sessions since around 2017. They take place every second weekend &mdash; Friday afternoon into the evening, Saturday and Sunday mornings &mdash; and are limited to around 7 artists in addition to myself and the model. All ability and skill levels are welcome, as are all shapes and sizes of models.</p>
            </div>
        </details>
    </div>

    <!-- For Models -->
    <div class="faq-section">
        <h3>For Models</h3>

        <details class="faq-item">
            <summary>How long or challenging will the poses be?</summary>
            <div class="faq-answer">
                <p>We try to gauge the abilities of the model so that we don&rsquo;t push the model too far out of their comfort zones, but we do tend to push a little bit.</p>
                <p>We usually start with a gestural dynamic movement session of around twenty minutes where the model is moving to music or holding short poses in succession, for both the artists and model to &ldquo;warm-up&rdquo;. Then usually we build up towards longer poses, e.g. 3 mins, 9 mins, 30 mins, break &amp; repeat, with usually a longer pose of 45 to 60 mins at the end of the day.</p>
                <p>The shorter poses are typically more &lsquo;dynamic&rsquo; i.e. standing, crouching etc. As the poses get longer they are typically seated or reclining.</p>
            </div>
        </details>

        <details class="faq-item">
            <summary>Do I have to be naked?</summary>
            <div class="faq-answer">
                <p>Yes, a large part of life-drawing is about figure study. However, we do use &lsquo;drapes&rsquo; either as loose sheets or e.g. a sarong. Your comfort is important and some times are easier to be nude than others &mdash; please discuss with me as necessary.</p>
            </div>
        </details>

        <details class="faq-item">
            <summary>Will there be photographs taken of me?</summary>
            <div class="faq-answer">
                <p>This depends on you. If you are okay with it, I like to take photographs, both for reference and fine-art photography purposes which I occasionally publish and/or exhibit, but it is up to the model and I send the images to the model for approval before doing either of those.</p>
            </div>
        </details>

